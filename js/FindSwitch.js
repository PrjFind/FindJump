function Sel(name){
    var objectIndex = array.findIndex(object => object.id == name);
    id=array[objectIndex].imgName;
    //调用setInterval()函数,每隔0.5秒隐藏显示
    imgsrc=document.getElementById(id).src;
    clock =  setInterval("getImg(id)",500);
}

function getImg(id) {
    imgId = document.getElementById(id);
    var str = imgId.src.split("/")
    var srcname = str[str.length-1]
    if(srcname=="flicker.JPG"){//图片隐藏
        imgId.src=imgsrc;//图片显示
    }else{
        imgId.src="img/flicker.JPG";//图片隐藏
    }
}

